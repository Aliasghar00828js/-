async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    kb = [['میدونی چرا؟ 🤔']]
    reply_markup = ReplyKeyboardMarkup(kb, resize_keyboard=True)
    await update.message.reply_text("سلام! به ربات جوک‌های بی‌مزه خوش اومدی. روی دکمه بزن تا یه جوک برات بگم! 👇", reply_markup=reply_markup)

async def send_joke(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if update.message.text == 'میدونی چرا؟ 🤔':
        joke = random.choice(JOKES)
        await update.message.reply_text(joke)

if __name__ == '__main__':
    token = os.environ.get('BOT_TOKEN')
    app = ApplicationBuilder().token(token).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & (~filters.COMMAND), send_joke))
    app.run_polling()
  
